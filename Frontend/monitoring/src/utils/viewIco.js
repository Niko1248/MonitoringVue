function viewIco(ico) {
  switch (ico) {
    case 'Лулу':
      return '/src/assets/img/payload/Лулу.svg'
    case 'Комар':
      return '/src/assets/img/payload/Комар.svg'
    case 'Питомник':
      return '/src/assets/img/payload/Питомник.svg'
    case 'Отец-одиночка':
      return '/src/assets/img/payload/Отец-одиночка.svg'
    case 'Васин ёж':
      return '/src/assets/img/payload/Васин ёж.svg'
    case 'Бобер':
      return '/src/assets/img/payload/Бобер.svg'
    case '40 градусов':
      return '/src/assets/img/payload/40 градусов.svg'
    case 'Зубатый собачник':
      return '/src/assets/img/payload/Зубатый собачник.svg'
  }
}

export default viewIco
